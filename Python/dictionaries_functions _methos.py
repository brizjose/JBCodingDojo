#DICTIONARIES BUILT-IN FUNCTIONS

#cmp(dict1, dict2)      compares dictionaries in the following order:
#                           length, key names, values
#len()                  returns length of dictionary
#str()                  produces a string representation of the dictionary
#type()                 returns the type of passed variable; if dictionary, returns _dict_

#DICTIONARIES METHODS

#.clear()                removes all elements from a dictionary
#.copy()                 returns a shallow copy of the dictionary
#.fromkeys(sequence,[value])    create dictionary with sequence as keys and [value] as values
#.get(key, default=None)       for key _key_, return its value or default if not in dictionary
#.has_key(key)           return true if key in dictionary, false if not
#.items()                return a list of dictionary pairs as tuples (key,value)
#.keys()                 return a list of dictionary keys
#.setdefault(key,default=None)   creates key with default value if key not already in dictionary
#.update(dict2)          adds dict2 key:value pairs to an existing dictionary
#.values()               returns a list of dictionary values

